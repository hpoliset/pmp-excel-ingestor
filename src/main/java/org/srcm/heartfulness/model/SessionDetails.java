package org.srcm.heartfulness.model;

import java.util.Date;

import com.fasterxml.jackson.annotation.JsonIgnore;
import com.fasterxml.jackson.annotation.JsonProperty;

/**
 * @author Koustav Dutta
 *
 */
public class SessionDetails {

	@JsonProperty("event_id")
	private String  eventId;

	@JsonIgnore
	private int programId;

	@JsonProperty("session_id")
	private String autoGeneratedSessionId;

	@JsonIgnore
	private int sessionId;

	@JsonProperty("session_number")
	private String sessionNumber;
	
	@JsonIgnore
	private Date sessionDate;
	
	@JsonProperty("session_date")
	private String sessionStringDate; 

	@JsonProperty("number_of_participants")
	private int numberOfParticipants;

	@JsonProperty("number_of_new_participants")
	private int numberOfNewParticipants;

	@JsonProperty("topic_covered")
	private String topicCovered;

	@JsonProperty("preceptor_name")
	private String preceptorName;

	@JsonProperty("preceptor_id_card_no")
	private String preceptorIdCardNo;

	@JsonProperty("comments")
	private String comments;
	
	@JsonIgnore
	private int isDeleted;
	
	@JsonIgnore
	private Date createTime;
	
	@JsonIgnore
	private Date updateTime;

	public String getEventId() {
		return eventId;
	}

	public void setEventId(String eventId) {
		this.eventId = eventId;
	}

	public int getProgramId() {
		return programId;
	}

	public void setProgramId(int programId) {
		this.programId = programId;
	}

	public String getAutoGeneratedSessionId() {
		return autoGeneratedSessionId;
	}

	public void setAutoGeneratedSessionId(String autoGeneratedSessionId) {
		this.autoGeneratedSessionId = autoGeneratedSessionId;
	}

	public int getSessionId() {
		return sessionId;
	}

	public void setSessionId(int sessionId) {
		this.sessionId = sessionId;
	}

	public String getSessionNumber() {
		return sessionNumber;
	}

	public void setSessionNumber(String sessionNumber) {
		this.sessionNumber = sessionNumber;
	}

	public Date getSessionDate() {
		return sessionDate;
	}

	public void setSessionDate(Date sessionDate) {
		this.sessionDate = sessionDate;
	}

	public String getSessionStringDate() {
		return sessionStringDate;
	}

	public void setSessionStringDate(String sessionStringDate) {
		this.sessionStringDate = sessionStringDate;
	}

	public int getNumberOfParticipants() {
		return numberOfParticipants;
	}

	public void setNumberOfParticipants(int numberOfParticipants) {
		this.numberOfParticipants = numberOfParticipants;
	}

	public int getNumberOfNewParticipants() {
		return numberOfNewParticipants;
	}

	public void setNumberOfNewParticipants(int numberOfNewParticipants) {
		this.numberOfNewParticipants = numberOfNewParticipants;
	}

	public String getTopicCovered() {
		return topicCovered;
	}

	public void setTopicCovered(String topicCovered) {
		this.topicCovered = topicCovered;
	}

	public String getPreceptorName() {
		return preceptorName;
	}

	public void setPreceptorName(String preceptorName) {
		this.preceptorName = preceptorName;
	}

	public String getPreceptorIdCardNo() {
		return preceptorIdCardNo;
	}

	public void setPreceptorIdCardNo(String preceptorIdCardNo) {
		this.preceptorIdCardNo = preceptorIdCardNo;
	}

	public String getComments() {
		return comments;
	}

	public void setComments(String comments) {
		this.comments = comments;
	}

	public int getIsDeleted() {
		return isDeleted;
	}

	public void setIsDeleted(int isDeleted) {
		this.isDeleted = isDeleted;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}

	@Override
	public String toString() {
		return "SessionDetails [eventId=" + eventId + ", programId=" + programId + ", autoGeneratedSessionId="
				+ autoGeneratedSessionId + ", sessionId=" + sessionId + ", sessionNumber=" + sessionNumber
				+ ", sessionDate=" + sessionDate + ", sessionStringDate=" + sessionStringDate
				+ ", numberOfParticipants=" + numberOfParticipants + ", numberOfNewParticipants="
				+ numberOfNewParticipants + ", topicCovered=" + topicCovered + ", preceptorName=" + preceptorName
				+ ", preceptorIdCardNo=" + preceptorIdCardNo + ", comments=" + comments + ", isDeleted=" + isDeleted
				+ ", createTime=" + createTime + ", updateTime=" + updateTime + "]";
	}

}
